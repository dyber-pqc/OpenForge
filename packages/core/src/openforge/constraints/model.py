def _tokenize_tcl(s: str) -> list[str]:
    """Simple TCL-ish tokenizer. Groups {...} and [...] together."""
    tokens: list[str] = []
    i = 0
    n = len(s)
    while i < n:
        ch = s[i]
        if ch.isspace():
            i += 1
            continue
        if ch == "{":
            depth = 1
            j = i + 1
            while j < n and depth:
                if s[j] == "{":
                    depth += 1
                elif s[j] == "}":
                    depth -= 1
                j += 1
            tokens.append(s[i:j])
            i = j
            continue
        if ch == "[":
            depth = 1
            j = i + 1
            while j < n and depth:
                if s[j] == "[":
                    depth += 1
                elif s[j] == "]":
                    depth -= 1
                j += 1
            tokens.append(s[i:j])
            i = j
            continue
        if ch == '"':
            j = i + 1
            while j < n and s[j] != '"':
                j += 1
            tokens.append(s[i : j + 1])
            i = j + 1
            continue
        j = i
        while j < n and not s[j].isspace():
            j += 1
        tokens.append(s[i:j])
        i = j
    return tokens


def _parse_sdc_line(line: str) -> Constraint | None:
    line = line.strip()
    if not line:
        return None
    toks = _tokenize_tcl(line)
    if not toks:
        return None
    head = toks[0].lower()
    rest = toks[1:]

    def _get_opt(name: str) -> str | None:
        for i, t in enumerate(rest):
            if t == name and i + 1 < len(rest):
                return rest[i + 1]
        return None

    def _has_flag(name: str) -> bool:
        return name in rest

    def _positional() -> list[str]:
        out: list[str] = []
        skip = False
        for _i, t in enumerate(rest):
            if skip:
                skip = False
                continue
            if t.startswith("-") and t not in ("-setup", "-hold", "-max", "-min"):
                skip = True
                continue
            if t in ("-setup", "-hold", "-max", "-min"):
                continue
            out.append(t)
        return out

    if head == "create_clock":
        name = _get_opt("-name") or ""
        period = _as_float(_get_opt("-period")) or 0.0
        waveform = _get_opt("-waveform")
        pos = _positional()
        target = pos[-1] if pos else ""
        if not name:
            name = _strip_get_ports(target) or "clk"
        attrs: dict[str, Any] = {}
        if waveform:
            nums = re.findall(r"[\d.]+", waveform)
            if len(nums) >= 2:
                attrs["waveform"] = [float(nums[0]), float(nums[1])]
        return Constraint(
            kind=ConstraintKind.CLOCK,
            name=name,
            target=target,
            value=period,
            units="ns",
            attrs=attrs,
        )
    if head == "create_generated_clock":
        name = _get_opt("-name") or ""
        source = _get_opt("-source")
        div = _get_opt("-divide_by")
        mul = _get_opt("-multiply_by")
        pos = _positional()
        target = pos[-1] if pos else ""
        attrs: dict[str, Any] = {}
        if source:
            attrs["source"] = source
        if div:
            attrs["divide_by"] = int(div)
        if mul:
            attrs["multiply_by"] = int(mul)
        return Constraint(
            kind=ConstraintKind.GENERATED_CLOCK,
            name=name,
            target=target,
            attrs=attrs,
        )
    if head == "set_input_delay":
        clk = _get_opt("-clock") or ""
        pos = _positional()
        if len(pos) < 2:
            return None
        delay = _as_float(pos[0]) or 0.0
        target = pos[1]
        attrs: dict[str, Any] = {"clock": clk}
        if _has_flag("-max"):
            attrs["max"] = True
        if _has_flag("-min"):
            attrs["min"] = True
        return Constraint(
            kind=ConstraintKind.INPUT_DELAY,
            target=target,
            value=delay,
            units="ns",
            attrs=attrs,
        )
    if head == "set_output_delay":
        clk = _get_opt("-clock") or ""
        pos = _positional()
        if len(pos) < 2:
            return None
        delay = _as_float(pos[0]) or 0.0
        target = pos[1]
        attrs: dict[str, Any] = {"clock": clk}
        if _has_flag("-max"):
            attrs["max"] = True
        if _has_flag("-min"):
            attrs["min"] = True
        return Constraint(
            kind=ConstraintKind.OUTPUT_DELAY,
            target=target,
            value=delay,
            units="ns",
            attrs=attrs,
        )
    if head == "set_false_path":
        attrs: dict[str, Any] = {}
        for k in ("-from", "-to", "-through"):
            v = _get_opt(k)
            if v:
                attrs[k.lstrip("-")] = v
        return Constraint(kind=ConstraintKind.FALSE_PATH, attrs=attrs)
    if head == "set_multicycle_path":
        pos = _positional()
        val = pos[0] if pos else "1"
        attrs = {}
        for k in ("-from", "-to", "-through"):
            v = _get_opt(k)
            if v:
                attrs[k.lstrip("-")] = v
        if _has_flag("-setup"):
            attrs["setup"] = True
        if _has_flag("-hold"):
            attrs["hold"] = True
        return Constraint(kind=ConstraintKind.MULTICYCLE_PATH, value=val, attrs=attrs)
    if head == "set_max_delay":
        pos = _positional()
        val = pos[0] if pos else "0"
        attrs = {}
        for k in ("-from", "-to", "-through"):
            v = _get_opt(k)
            if v:
                attrs[k.lstrip("-")] = v
        return Constraint(kind=ConstraintKind.MAX_DELAY, value=val, attrs=attrs)
    if head == "set_min_delay":
        pos = _positional()
        val = pos[0] if pos else "0"
        attrs = {}
        for k in ("-from", "-to", "-through"):
            v = _get_opt(k)
            if v:
                attrs[k.lstrip("-")] = v
        return Constraint(kind=ConstraintKind.MIN_DELAY, value=val, attrs=attrs)
    if head == "set_load":
        pos = _positional()
        if len(pos) >= 2:
            return Constraint(kind=ConstraintKind.SET_LOAD, value=pos[0], target=pos[1])
    if head == "set_drive":
        pos = _positional()
        if len(pos) >= 2:
            return Constraint(kind=ConstraintKind.SET_DRIVE, value=pos[0], target=pos[1])
    if head == "group_path":
        name = _get_opt("-name") or ""
        attrs = {}
        for k in ("-from", "-to", "-weight"):
            v = _get_opt(k)
            if v:
                attrs[k.lstrip("-")] = v
        return Constraint(kind=ConstraintKind.GROUP_PATH, name=name, attrs=attrs)
    if head == "set_case_analysis":
        pos = _positional()
        if len(pos) >= 2:
            return Constraint(kind=ConstraintKind.CASE_ANALYSIS, value=pos[0], target=pos[1])
    return None
