"""Unified constraint model with SDC/XDC/LPF/PCF/CST serialization.

This module provides a tool-agnostic representation of FPGA/ASIC timing and
physical constraints. A ``ConstraintSet`` can be emitted as:

- SDC  -- Synopsys Design Constraints (OpenROAD, Yosys, Quartus, etc.)
- XDC  -- Xilinx Design Constraints (Vivado) -- SDC superset + ``set_property``
- LPF  -- Lattice Preference File (ECP5, project Trellis / nextpnr)
- PCF  -- Physical Constraints File (iCE40, project IceStorm / nextpnr)
- CST  -- Gowin Constraint file (GW1N / GW2A families)

And parsed back from the same. The model is intentionally loose (free-form
``attrs`` dict) so we can round-trip vendor-specific keys we don't model
explicitly.
"""

from __future__ import annotations

import re
from enum import StrEnum
from typing import Any

from pydantic import BaseModel, ConfigDict, Field


class ConstraintKind(StrEnum):
    """Kind of constraint. Covers both timing (SDC) and physical (IO)."""

    CLOCK = "clock"
    GENERATED_CLOCK = "generated_clock"
    INPUT_DELAY = "input_delay"
    OUTPUT_DELAY = "output_delay"
    FALSE_PATH = "false_path"
    MULTICYCLE_PATH = "multicycle_path"
    MAX_DELAY = "max_delay"
    MIN_DELAY = "min_delay"
    SET_LOAD = "set_load"
    SET_DRIVE = "set_drive"
    GROUP_PATH = "group_path"
    CASE_ANALYSIS = "case_analysis"
    IO_STANDARD = "io_standard"
    PIN_LOCATION = "pin_location"
    DRIVE_STRENGTH = "drive_strength"
    SLEW_RATE = "slew_rate"
    PULL = "pull"


class Constraint(BaseModel):
    """A single constraint entry.

    ``target`` is a port / pin / net name (for physical constraints) or a
    collection reference like ``[get_ports clk]`` for SDC-style commands.
    ``value`` holds the primary numeric or string value (period, delay, pin).
    ``attrs`` holds extra key/value pairs (waveform, clock ref, from/to/through).
    """

    model_config = ConfigDict(extra="allow")

    kind: ConstraintKind
    name: str = ""
    target: str = ""
    value: Any = None
    units: str = ""
    attrs: dict[str, Any] = Field(default_factory=dict)


