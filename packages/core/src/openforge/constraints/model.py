_SDC_KINDS = {
    ConstraintKind.CLOCK,
    ConstraintKind.GENERATED_CLOCK,
    ConstraintKind.INPUT_DELAY,
    ConstraintKind.OUTPUT_DELAY,
    ConstraintKind.FALSE_PATH,
    ConstraintKind.MULTICYCLE_PATH,
    ConstraintKind.MAX_DELAY,
    ConstraintKind.MIN_DELAY,
    ConstraintKind.SET_LOAD,
    ConstraintKind.SET_DRIVE,
    ConstraintKind.GROUP_PATH,
    ConstraintKind.CASE_ANALYSIS,
}

_XDC_PROP_TO_KIND: dict[str, ConstraintKind] = {
    "PACKAGE_PIN": ConstraintKind.PIN_LOCATION,
    "LOC": ConstraintKind.PIN_LOCATION,
    "IOSTANDARD": ConstraintKind.IO_STANDARD,
    "DRIVE": ConstraintKind.DRIVE_STRENGTH,
    "SLEW": ConstraintKind.SLEW_RATE,
    "PULLUP": ConstraintKind.PULL,
    "PULLDOWN": ConstraintKind.PULL,
    "KEEPER": ConstraintKind.PULL,
}


def _as_float(v: Any) -> float | None:
    try:
        return float(v)
    except (TypeError, ValueError):
        return None


def _strip_get_ports(expr: str) -> str:
    if not expr:
        return ""
    m = re.search(r"get_ports\s+\{?([^\]\}]+)\}?", expr)
    if m:
        return m.group(1).strip()
    return expr.strip("[] {}")


def _iter_tcl_lines(text: str):
    """Yield logical TCL lines, handling backslash continuation and comments."""
    buf: list[str] = []
    for raw in text.splitlines():
        # strip full-line comment
        if raw.lstrip().startswith("#"):
            continue
        s = raw.rstrip()
        if s.endswith("\\"):
            buf.append(s[:-1])
            continue
        buf.append(s)
        line = " ".join(x.strip() for x in buf if x.strip())
        buf.clear()
        if line:
            yield line


def _sdc_line(c: Constraint) -> str:
    k = c.kind
    if k == ConstraintKind.CLOCK:
        # create_clock -name clk -period 10.0 [-waveform {0 5}] [get_ports clk]
        period = _as_float(c.value) or 0.0
        name = c.name or _strip_get_ports(c.target) or "clk"
        parts = ["create_clock", "-name", name, "-period", f"{period:g}"]
        wf = c.attrs.get("waveform")
        if isinstance(wf, (list, tuple)) and len(wf) == 2:
            parts += ["-waveform", f"{{{wf[0]} {wf[1]}}}"]
        tgt = c.target or f"[get_ports {name}]"
        parts.append(tgt)
        return " ".join(parts)
    if k == ConstraintKind.GENERATED_CLOCK:
        parts = ["create_generated_clock", "-name", c.name]
        if "source" in c.attrs:
            parts += ["-source", str(c.attrs["source"])]
        if "divide_by" in c.attrs:
            parts += ["-divide_by", str(c.attrs["divide_by"])]
        if "multiply_by" in c.attrs:
            parts += ["-multiply_by", str(c.attrs["multiply_by"])]
        parts.append(c.target or f"[get_pins {c.name}]")
        return " ".join(parts)
    if k == ConstraintKind.INPUT_DELAY:
        clk = c.attrs.get("clock", "clk")
        delay = _as_float(c.value) or 0.0
        flag = ""
        if c.attrs.get("max"):
            flag = " -max"
        elif c.attrs.get("min"):
            flag = " -min"
        return f"set_input_delay -clock {clk}{flag} {delay:g} {c.target}"
    if k == ConstraintKind.OUTPUT_DELAY:
        clk = c.attrs.get("clock", "clk")
        delay = _as_float(c.value) or 0.0
        flag = ""
        if c.attrs.get("max"):
            flag = " -max"
        elif c.attrs.get("min"):
            flag = " -min"
        return f"set_output_delay -clock {clk}{flag} {delay:g} {c.target}"
    if k == ConstraintKind.FALSE_PATH:
        parts = ["set_false_path"]
        if "from" in c.attrs:
            parts += ["-from", str(c.attrs["from"])]
        if "to" in c.attrs:
            parts += ["-to", str(c.attrs["to"])]
        if "through" in c.attrs:
            parts += ["-through", str(c.attrs["through"])]
        return " ".join(parts)
    if k == ConstraintKind.MULTICYCLE_PATH:
        parts = ["set_multicycle_path", str(c.value)]
        if c.attrs.get("setup"):
            parts.append("-setup")
        if c.attrs.get("hold"):
            parts.append("-hold")
        if "from" in c.attrs:
            parts += ["-from", str(c.attrs["from"])]
        if "to" in c.attrs:
            parts += ["-to", str(c.attrs["to"])]
        if "through" in c.attrs:
            parts += ["-through", str(c.attrs["through"])]
        return " ".join(parts)
    if k == ConstraintKind.MAX_DELAY:
        parts = ["set_max_delay", str(c.value)]
        if "from" in c.attrs:
            parts += ["-from", str(c.attrs["from"])]
        if "to" in c.attrs:
            parts += ["-to", str(c.attrs["to"])]
        return " ".join(parts)
    if k == ConstraintKind.MIN_DELAY:
        parts = ["set_min_delay", str(c.value)]
        if "from" in c.attrs:
            parts += ["-from", str(c.attrs["from"])]
        if "to" in c.attrs:
            parts += ["-to", str(c.attrs["to"])]
        return " ".join(parts)
    if k == ConstraintKind.SET_LOAD:
        return f"set_load {c.value} {c.target}"
    if k == ConstraintKind.SET_DRIVE:
        return f"set_drive {c.value} {c.target}"
    if k == ConstraintKind.GROUP_PATH:
        parts = ["group_path", "-name", c.name]
        if "from" in c.attrs:
            parts += ["-from", str(c.attrs["from"])]
        if "to" in c.attrs:
            parts += ["-to", str(c.attrs["to"])]
        if "weight" in c.attrs:
            parts += ["-weight", str(c.attrs["weight"])]
        return " ".join(parts)
    if k == ConstraintKind.CASE_ANALYSIS:
        return f"set_case_analysis {c.value} {c.target}"
    return ""


_CLOCK_RE = re.compile(r"^create_clock\s+(.*)$", re.IGNORECASE)
_GEN_CLOCK_RE = re.compile(r"^create_generated_clock\s+(.*)$", re.IGNORECASE)
