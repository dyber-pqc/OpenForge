class ConstraintSet(BaseModel):
    """A named collection of constraints with multi-format I/O."""

    model_config = ConfigDict(extra="allow")

    name: str = "default"
    constraints: list[Constraint] = Field(default_factory=list)

    # ── add helpers ─────────────────────────────────────────────────────────

    def add(self, c: Constraint) -> ConstraintSet:
        self.constraints.append(c)
        return self

    def extend(self, cs: list[Constraint]) -> ConstraintSet:
        self.constraints.extend(cs)
        return self

    def by_kind(self, kind: ConstraintKind) -> list[Constraint]:
        return [c for c in self.constraints if c.kind == kind]

    # ── SDC ─────────────────────────────────────────────────────────────────

    def to_sdc(self) -> str:
        """Emit Synopsys Design Constraints text."""
        lines: list[str] = [
            f"# OpenForge SDC -- {self.name}",
            "# Generated by openforge.constraints",
        ]
        for c in self.constraints:
            line = _sdc_line(c)
            if line:
                lines.append(line)
        return "\n".join(lines) + "\n"

    @classmethod
    def from_sdc(cls, text: str, name: str = "default") -> ConstraintSet:
        cs = cls(name=name)
        for raw in _iter_tcl_lines(text):
            c = _parse_sdc_line(raw)
            if c is not None:
                cs.constraints.append(c)
        return cs

    # ── XDC (Vivado) ────────────────────────────────────────────────────────

    def to_xdc(self) -> str:
        """Emit Xilinx XDC text. XDC is SDC + ``set_property`` for IO."""
        lines: list[str] = [
            f"# OpenForge XDC -- {self.name}",
            "# Vivado constraint file",
        ]
        for c in self.constraints:
            if c.kind in _SDC_KINDS:
                line = _sdc_line(c)
                if line:
                    lines.append(line)
            elif c.kind == ConstraintKind.PIN_LOCATION:
                # set_property PACKAGE_PIN H16 [get_ports clk]
                port = c.target or c.name
                lines.append(f"set_property PACKAGE_PIN {c.value} [get_ports {{{port}}}]")
            elif c.kind == ConstraintKind.IO_STANDARD:
                port = c.target or c.name
                lines.append(f"set_property IOSTANDARD {c.value} [get_ports {{{port}}}]")
            elif c.kind == ConstraintKind.DRIVE_STRENGTH:
                port = c.target or c.name
                lines.append(f"set_property DRIVE {c.value} [get_ports {{{port}}}]")
            elif c.kind == ConstraintKind.SLEW_RATE:
                port = c.target or c.name
                lines.append(f"set_property SLEW {c.value} [get_ports {{{port}}}]")
            elif c.kind == ConstraintKind.PULL:
                port = c.target or c.name
                # Vivado: PULLUP / PULLDOWN / PULLNONE / KEEPER -> via set_property
                v = str(c.value).upper()
                if v in ("UP", "PULLUP"):
                    prop = "PULLUP TRUE"
                elif v in ("DOWN", "PULLDOWN"):
                    prop = "PULLDOWN TRUE"
                elif v in ("KEEPER",):
                    prop = "KEEPER TRUE"
                else:
                    prop = "PULLUP FALSE"
                key, val = prop.split(" ", 1)
                lines.append(f"set_property {key} {val} [get_ports {{{port}}}]")
        return "\n".join(lines) + "\n"

    @classmethod
    def from_xdc(cls, text: str, name: str = "default") -> ConstraintSet:
        cs = cls(name=name)
        # set_property PROP VAL [get_ports {name}]
        set_prop_re = re.compile(
            r"^\s*set_property\s+(\S+)\s+(\S+)\s+\[\s*get_ports\s+\{?([^\]\}]+)\}?\s*\]",
            re.IGNORECASE,
        )
        for raw in _iter_tcl_lines(text):
            m = set_prop_re.match(raw)
            if m:
                prop, val, port = m.group(1), m.group(2), m.group(3).strip()
                kind = _XDC_PROP_TO_KIND.get(prop.upper())
                if kind is not None:
                    # Handle booleans for PULL
                    if kind == ConstraintKind.PULL:
                        if prop.upper() == "PULLUP" and val.upper() == "TRUE":
                            val = "UP"
                        elif prop.upper() == "PULLDOWN" and val.upper() == "TRUE":
                            val = "DOWN"
                        elif prop.upper() == "KEEPER" and val.upper() == "TRUE":
                            val = "KEEPER"
                        else:
                            continue
                    cs.constraints.append(Constraint(kind=kind, target=port, value=val))
                    continue
            # Fallback to SDC
            c = _parse_sdc_line(raw)
            if c is not None:
                cs.constraints.append(c)
        return cs

    # ── LPF (Lattice ECP5 / nextpnr-ecp5) ───────────────────────────────────

    def to_lpf(self) -> str:
        """Emit Lattice Preference File (LPF).

        Real LPF syntax as consumed by nextpnr-ecp5 / Diamond:
            LOCATE COMP "led" SITE "E16";
            IOBUF PORT "led" IO_TYPE=LVCMOS33 DRIVE=8;
            FREQUENCY PORT "clk" 25.000000 MHz;
        """
        lines: list[str] = [
            f"# OpenForge LPF -- {self.name}",
            "BLOCK RESETPATHS;",
            "BLOCK ASYNCPATHS;",
        ]
        # Aggregate per-port IOBUF props (IO_TYPE/DRIVE/SLEW/PULL) so we only
        # emit one IOBUF line per port.
        iobuf: dict[str, dict[str, str]] = {}
        for c in self.constraints:
            port = c.target or c.name
            if c.kind == ConstraintKind.PIN_LOCATION:
                lines.append(f'LOCATE COMP "{port}" SITE "{c.value}";')
            elif c.kind == ConstraintKind.IO_STANDARD:
                iobuf.setdefault(port, {})["IO_TYPE"] = str(c.value)
            elif c.kind == ConstraintKind.DRIVE_STRENGTH:
                iobuf.setdefault(port, {})["DRIVE"] = str(c.value)
            elif c.kind == ConstraintKind.SLEW_RATE:
                iobuf.setdefault(port, {})["SLEWRATE"] = str(c.value).upper()
            elif c.kind == ConstraintKind.PULL:
                v = str(c.value).upper()
                if v in ("UP", "PULLUP"):
                    iobuf.setdefault(port, {})["PULLMODE"] = "UP"
                elif v in ("DOWN", "PULLDOWN"):
                    iobuf.setdefault(port, {})["PULLMODE"] = "DOWN"
                elif v in ("NONE",):
                    iobuf.setdefault(port, {})["PULLMODE"] = "NONE"
            elif c.kind == ConstraintKind.CLOCK:
                period = _as_float(c.value)
                if period and period > 0:
                    freq = 1000.0 / period  # period ns -> MHz
                    tgt = c.attrs.get("port") or c.target or c.name
                    # Strip [get_ports ...] if present
                    tgt = _strip_get_ports(tgt)
                    lines.append(f'FREQUENCY PORT "{tgt}" {freq:.6f} MHz;')
        for port, props in iobuf.items():
            parts = " ".join(f"{k}={v}" for k, v in props.items())
            lines.append(f'IOBUF PORT "{port}" {parts};')
        return "\n".join(lines) + "\n"

    @classmethod
    def from_lpf(cls, text: str, name: str = "default") -> ConstraintSet:
        cs = cls(name=name)
        locate_re = re.compile(
            r'^\s*LOCATE\s+COMP\s+"([^"]+)"\s+SITE\s+"([^"]+)"\s*;',
            re.IGNORECASE,
        )
        iobuf_re = re.compile(r'^\s*IOBUF\s+PORT\s+"([^"]+)"\s+(.*?);', re.IGNORECASE)
        freq_re = re.compile(
            r'^\s*FREQUENCY\s+PORT\s+"([^"]+)"\s+([\d.]+)\s*MHz\s*;',
            re.IGNORECASE,
        )
        for raw in text.splitlines():
            line = raw.strip()
            if not line or line.startswith("#") or line.startswith("//"):
                continue
            m = locate_re.match(line)
            if m:
                cs.constraints.append(
                    Constraint(
                        kind=ConstraintKind.PIN_LOCATION,
                        target=m.group(1),
                        value=m.group(2),
                    )
                )
                continue
            m = iobuf_re.match(line)
            if m:
                port, body = m.group(1), m.group(2)
                for kv in body.split():
                    if "=" not in kv:
                        continue
                    k, v = kv.split("=", 1)
                    k = k.strip().upper()
                    v = v.strip()
                    if k == "IO_TYPE":
                        cs.constraints.append(
                            Constraint(
                                kind=ConstraintKind.IO_STANDARD,
                                target=port,
                                value=v,
                            )
                        )
                    elif k == "DRIVE":
                        cs.constraints.append(
                            Constraint(
                                kind=ConstraintKind.DRIVE_STRENGTH,
                                target=port,
                                value=v,
                            )
                        )
                    elif k == "SLEWRATE":
                        cs.constraints.append(
                            Constraint(
                                kind=ConstraintKind.SLEW_RATE,
                                target=port,
                                value=v,
                            )
                        )
                    elif k == "PULLMODE":
                        cs.constraints.append(
                            Constraint(
                                kind=ConstraintKind.PULL,
                                target=port,
                                value=v,
                            )
                        )
                continue
            m = freq_re.match(line)
            if m:
                port, mhz = m.group(1), float(m.group(2))
                period = 1000.0 / mhz if mhz > 0 else 0.0
                cs.constraints.append(
                    Constraint(
                        kind=ConstraintKind.CLOCK,
                        name=port,
                        target=f"[get_ports {port}]",
                        value=period,
                        units="ns",
                        attrs={"port": port, "freq_mhz": mhz},
                    )
                )
        return cs

    # ── PCF (iCE40 / nextpnr-ice40 / arachne-pnr) ───────────────────────────

    def to_pcf(self) -> str:
        """Emit PCF (Physical Constraints File).

        Real PCF syntax as used by nextpnr-ice40 / IceStorm:
            set_io led 95
            set_io -pullup yes btn 63
        """
        lines: list[str] = [f"# OpenForge PCF -- {self.name}"]
        pull_map: dict[str, str] = {}
        for c in self.constraints:
            if c.kind == ConstraintKind.PULL:
                port = c.target or c.name
                v = str(c.value).upper()
                if v in ("UP", "PULLUP"):
                    pull_map[port] = "yes"
        for c in self.constraints:
            if c.kind == ConstraintKind.PIN_LOCATION:
                port = c.target or c.name
                pin = c.value
                pu = pull_map.get(port)
                if pu:
                    lines.append(f"set_io -pullup {pu} {port} {pin}")
                else:
                    lines.append(f"set_io {port} {pin}")
        return "\n".join(lines) + "\n"

    @classmethod
    def from_pcf(cls, text: str, name: str = "default") -> ConstraintSet:
        cs = cls(name=name)
        for raw in text.splitlines():
            line = raw.split("#", 1)[0].strip()
            if not line:
                continue
            if not line.startswith("set_io"):
                continue
            toks = line.split()
            # set_io [-nowarn] [-pullup yes|no] [-warn-no-port] port pin
            pullup: str | None = None
            i = 1
            port: str | None = None
            pin: str | None = None
            while i < len(toks):
                t = toks[i]
                if t == "-pullup" and i + 1 < len(toks):
                    pullup = toks[i + 1]
                    i += 2
                    continue
                if t.startswith("-"):
                    i += 1
                    continue
                if port is None:
                    port = t
                elif pin is None:
                    pin = t
                i += 1
            if port and pin:
                cs.constraints.append(
                    Constraint(
                        kind=ConstraintKind.PIN_LOCATION,
                        target=port,
                        value=pin,
                    )
                )
                if pullup and pullup.lower() in ("yes", "true", "1"):
                    cs.constraints.append(
                        Constraint(
                            kind=ConstraintKind.PULL,
                            target=port,
                            value="UP",
                        )
                    )
        return cs

    # ── CST (Gowin) ─────────────────────────────────────────────────────────

    def to_cst(self) -> str:
        """Emit Gowin CST text.

        Real CST syntax:
            IO_LOC "led[0]" 10;
            IO_PORT "led[0]" IO_TYPE=LVCMOS33 DRIVE=8 PULL_MODE=NONE;
        """
        lines: list[str] = [f"// OpenForge CST -- {self.name}"]
        port_attrs: dict[str, dict[str, str]] = {}
        for c in self.constraints:
            port = c.target or c.name
            if c.kind == ConstraintKind.PIN_LOCATION:
                lines.append(f'IO_LOC "{port}" {c.value};')
            elif c.kind == ConstraintKind.IO_STANDARD:
                port_attrs.setdefault(port, {})["IO_TYPE"] = str(c.value)
            elif c.kind == ConstraintKind.DRIVE_STRENGTH:
                port_attrs.setdefault(port, {})["DRIVE"] = str(c.value)
            elif c.kind == ConstraintKind.SLEW_RATE:
                port_attrs.setdefault(port, {})["SLEW_RATE"] = str(c.value).upper()
            elif c.kind == ConstraintKind.PULL:
                v = str(c.value).upper()
                mode = {
                    "UP": "UP",
                    "PULLUP": "UP",
                    "DOWN": "DOWN",
                    "PULLDOWN": "DOWN",
                    "NONE": "NONE",
                    "KEEPER": "KEEPER",
                }.get(v, "NONE")
                port_attrs.setdefault(port, {})["PULL_MODE"] = mode
        for port, props in port_attrs.items():
            parts = " ".join(f"{k}={v}" for k, v in props.items())
            lines.append(f'IO_PORT "{port}" {parts};')
        # Clock frequency comment (Gowin uses SDC for timing; CST is IO only)
        for c in self.by_kind(ConstraintKind.CLOCK):
            period = _as_float(c.value)
            if period and period > 0:
                freq = 1000.0 / period
                lines.append(
                    f"// create_clock {_strip_get_ports(c.target) or c.name} {freq:.3f}MHz"
                )
        return "\n".join(lines) + "\n"

    @classmethod
    def from_cst(cls, text: str, name: str = "default") -> ConstraintSet:
        cs = cls(name=name)
        io_loc_re = re.compile(r'^\s*IO_LOC\s+"([^"]+)"\s+([A-Za-z0-9_/]+)\s*;', re.IGNORECASE)
        io_port_re = re.compile(r'^\s*IO_PORT\s+"([^"]+)"\s+(.*?);', re.IGNORECASE)
        for raw in text.splitlines():
            line = raw.strip()
            if not line or line.startswith("//") or line.startswith("#"):
                continue
            m = io_loc_re.match(line)
            if m:
                cs.constraints.append(
                    Constraint(
                        kind=ConstraintKind.PIN_LOCATION,
                        target=m.group(1),
                        value=m.group(2),
                    )
                )
                continue
            m = io_port_re.match(line)
            if m:
                port, body = m.group(1), m.group(2)
                for kv in body.split():
                    if "=" not in kv:
                        continue
                    k, v = kv.split("=", 1)
                    k = k.strip().upper()
                    v = v.strip()
                    if k == "IO_TYPE":
                        cs.constraints.append(
                            Constraint(
                                kind=ConstraintKind.IO_STANDARD,
                                target=port,
                                value=v,
                            )
                        )
                    elif k == "DRIVE":
                        cs.constraints.append(
                            Constraint(
                                kind=ConstraintKind.DRIVE_STRENGTH,
                                target=port,
                                value=v,
                            )
                        )
                    elif k == "SLEW_RATE":
                        cs.constraints.append(
                            Constraint(
                                kind=ConstraintKind.SLEW_RATE,
                                target=port,
                                value=v,
                            )
                        )
                    elif k == "PULL_MODE":
                        cs.constraints.append(
                            Constraint(
                                kind=ConstraintKind.PULL,
                                target=port,
                                value=v,
                            )
                        )
        return cs


