def dma_engine(
    addr_width: int = 32, data_width: int = 64, fifo_depth: int = 1024
) -> tuple[str, BlockInstance]:
    mod = "dma_engine"
    v = _banner(mod, "Scatter-gather DMA engine")
    v += f"""module {mod} #(
    parameter integer AW = {addr_width},
    parameter integer DW = {data_width},
    parameter integer FIFO = {fifo_depth}
) (
    input  wire            aclk,
    input  wire            aresetn,
    // Control registers (AXI-Lite slave)
    input  wire [AW-1:0]   ctrl_src_addr,
    input  wire [AW-1:0]   ctrl_dst_addr,
    input  wire [31:0]     ctrl_len,
    input  wire            ctrl_start,
    output reg             ctrl_done,
    output reg             ctrl_busy,
    // AXI4 read master
    output reg  [AW-1:0]   m_axi_araddr,
    output reg             m_axi_arvalid,
    input  wire            m_axi_arready,
    input  wire [DW-1:0]   m_axi_rdata,
    input  wire            m_axi_rvalid,
    output wire            m_axi_rready,
    input  wire            m_axi_rlast,
    // AXI4 write master
    output reg  [AW-1:0]   m_axi_awaddr,
    output reg             m_axi_awvalid,
    input  wire            m_axi_awready,
    output wire [DW-1:0]   m_axi_wdata,
    output reg             m_axi_wvalid,
    input  wire            m_axi_wready,
    output wire            m_axi_wlast,
    input  wire            m_axi_bvalid,
    output wire            m_axi_bready,
    // Interrupt
    output reg             irq
);
    reg [31:0] remaining;
    reg [DW-1:0] fifo [0:FIFO-1];
    reg [$clog2(FIFO+1)-1:0] fifo_wr, fifo_rd;
    wire fifo_empty = (fifo_wr == fifo_rd);
    wire fifo_full  = ((fifo_wr - fifo_rd) == FIFO);

    assign m_axi_rready = !fifo_full;
    assign m_axi_wdata  = fifo[fifo_rd[$clog2(FIFO)-1:0]];
    assign m_axi_wlast  = (remaining == (DW/8));
    assign m_axi_bready = 1'b1;

    always @(posedge aclk or negedge aresetn) begin
        if (!aresetn) begin
            ctrl_busy <= 0; ctrl_done <= 0; irq <= 0;
            m_axi_arvalid <= 0; m_axi_awvalid <= 0; m_axi_wvalid <= 0;
            fifo_wr <= 0; fifo_rd <= 0; remaining <= 0;
            m_axi_araddr <= 0; m_axi_awaddr <= 0;
        end else begin
            ctrl_done <= 0;
            if (ctrl_start && !ctrl_busy) begin
                ctrl_busy    <= 1;
                remaining    <= ctrl_len;
                m_axi_araddr <= ctrl_src_addr;
                m_axi_awaddr <= ctrl_dst_addr;
                m_axi_arvalid <= 1;
                m_axi_awvalid <= 1;
            end
            if (m_axi_arvalid && m_axi_arready) m_axi_arvalid <= 0;
            if (m_axi_awvalid && m_axi_awready) m_axi_awvalid <= 0;
            if (m_axi_rvalid && m_axi_rready && !fifo_full) begin
                fifo[fifo_wr[$clog2(FIFO)-1:0]] <= m_axi_rdata;
                fifo_wr <= fifo_wr + 1;
            end
            if (!fifo_empty && !m_axi_wvalid) m_axi_wvalid <= 1;
            if (m_axi_wvalid && m_axi_wready) begin
                m_axi_wvalid <= 0;
                fifo_rd <= fifo_rd + 1;
                remaining <= (remaining > (DW/8)) ? remaining - (DW/8) : 0;
            end
            if (ctrl_busy && remaining == 0 && fifo_empty) begin
                ctrl_busy <= 0;
                ctrl_done <= 1;
                irq <= 1;
            end
            if (!ctrl_start) irq <= 0;
        end
    end
"""
    v += _footer()
    inst = BlockInstance(
        name=mod, module=mod,
        params={"AW": addr_width, "DW": data_width, "FIFO": fifo_depth},
        ports=[
            BlockPort("aclk", "input", 1),
            BlockPort("aresetn", "input", 1),
            BlockPort("ctrl_src_addr", "input", addr_width),
            BlockPort("ctrl_dst_addr", "input", addr_width),
            BlockPort("ctrl_len", "input", 32),
            BlockPort("ctrl_start", "input", 1),
            BlockPort("ctrl_done", "output", 1),
            BlockPort("ctrl_busy", "output", 1),
            BlockPort("m_axi_araddr", "output", addr_width),
            BlockPort("m_axi_arvalid", "output", 1),
            BlockPort("m_axi_arready", "input", 1),
            BlockPort("m_axi_rdata", "input", data_width),
            BlockPort("m_axi_rvalid", "input", 1),
            BlockPort("m_axi_rready", "output", 1),
            BlockPort("m_axi_rlast", "input", 1),
            BlockPort("m_axi_awaddr", "output", addr_width),
            BlockPort("m_axi_awvalid", "output", 1),
            BlockPort("m_axi_awready", "input", 1),
            BlockPort("m_axi_wdata", "output", data_width),
            BlockPort("m_axi_wvalid", "output", 1),
            BlockPort("m_axi_wready", "input", 1),
            BlockPort("m_axi_wlast", "output", 1),
            BlockPort("m_axi_bvalid", "input", 1),
            BlockPort("m_axi_bready", "output", 1),
            BlockPort("irq", "output", 1),
        ],
        description="Scatter-gather DMA engine",
    )
    return v, inst


