def interrupt_controller_plic(
    num_irqs: int = 32, num_targets: int = 1, num_priorities: int = 8
) -> tuple[str, BlockInstance]:
    mod = f"plic_{num_irqs}x{num_targets}"
    prio_bits = max(1, (num_priorities - 1).bit_length())
    v = _banner(mod, f"PLIC {num_irqs} IRQs x {num_targets} targets")
    v += f"""module {mod} #(
    parameter integer N_IRQ    = {num_irqs},
    parameter integer N_TARGET = {num_targets},
    parameter integer N_PRIO   = {num_priorities}
) (
    input  wire                 clk,
    input  wire                 rstn,
    input  wire [N_IRQ-1:0]     irq_sources,
    input  wire [N_IRQ*{prio_bits}-1:0] irq_priority,
    input  wire [N_IRQ-1:0]     irq_enable,
    output reg  [N_TARGET-1:0]  target_irq,
    output reg  [$clog2(N_IRQ+1)-1:0] claim_id
);
    integer i;
    reg [{prio_bits}-1:0] best_prio;
    reg [$clog2(N_IRQ+1)-1:0] best_id;
    always @(posedge clk or negedge rstn) begin
        if (!rstn) begin
            target_irq <= 0;
            claim_id   <= 0;
        end else begin
            best_prio = 0;
            best_id   = 0;
            for (i = 0; i < N_IRQ; i = i + 1) begin
                if (irq_sources[i] && irq_enable[i]) begin
                    if (irq_priority[i*{prio_bits} +: {prio_bits}] > best_prio) begin
                        best_prio = irq_priority[i*{prio_bits} +: {prio_bits}];
                        best_id   = i[$clog2(N_IRQ+1)-1:0] + 1;
                    end
                end
            end
            claim_id   <= best_id;
            target_irq <= {{N_TARGET{{|best_prio}}}};
        end
    end
"""
    v += _footer()
    inst = BlockInstance(
        name=mod, module=mod,
        params={"N_IRQ": num_irqs, "N_TARGET": num_targets, "N_PRIO": num_priorities},
        ports=[
            BlockPort("clk", "input", 1),
            BlockPort("rstn", "input", 1),
            BlockPort("irq_sources", "input", num_irqs),
            BlockPort("irq_priority", "input", num_irqs * prio_bits),
            BlockPort("irq_enable", "input", num_irqs),
            BlockPort("target_irq", "output", num_targets),
            BlockPort("claim_id", "output", max(1, (num_irqs + 1).bit_length())),
        ],
        description=f"PLIC {num_irqs}x{num_targets}",
    )
    return v, inst


