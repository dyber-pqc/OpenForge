def axi_stream_switch(
    num_inputs: int, num_outputs: int, width: int, route_table: list[int]
) -> tuple[str, BlockInstance]:
    mod = f"axis_switch_{num_inputs}x{num_outputs}_{width}"
    src = [_banner(mod, f"AXI-Stream {num_inputs}x{num_outputs} switch")]
    src.append(f"module {mod} #(parameter integer DW = {width}) (")
    src.append("    input  wire aclk,")
    src.append("    input  wire aresetn,")
    for i in range(num_inputs):
        src.append(f"    input  wire [DW-1:0] s{i:02d}_axis_tdata,")
        src.append(f"    input  wire          s{i:02d}_axis_tvalid,")
        src.append(f"    output wire          s{i:02d}_axis_tready,")
        src.append(f"    input  wire          s{i:02d}_axis_tlast,")
    for j in range(num_outputs):
        last = j == num_outputs - 1
        comma = "" if last else ","
        src.append(f"    output wire [DW-1:0] m{j:02d}_axis_tdata,")
        src.append(f"    output wire          m{j:02d}_axis_tvalid,")
        src.append(f"    input  wire          m{j:02d}_axis_tready,")
        src.append(f"    output wire          m{j:02d}_axis_tlast{comma}")
    src.append(");")
    # Fixed routing: master j receives from slave route_table[j]
    for j in range(num_outputs):
        i = route_table[j] if j < len(route_table) else 0
        src.append(f"    // output {j} <- input {i}")
        src.append(f"    assign m{j:02d}_axis_tdata  = s{i:02d}_axis_tdata;")
        src.append(f"    assign m{j:02d}_axis_tvalid = s{i:02d}_axis_tvalid;")
        src.append(f"    assign m{j:02d}_axis_tlast  = s{i:02d}_axis_tlast;")
    for i in range(num_inputs):
        backs = [
            f"m{j:02d}_axis_tready"
            for j in range(num_outputs)
            if (j < len(route_table) and route_table[j] == i)
        ]
        expr = " | ".join(backs) if backs else "1'b0"
        src.append(f"    assign s{i:02d}_axis_tready = {expr};")
    src.append(_footer())
    ports = [BlockPort("aclk", "input", 1), BlockPort("aresetn", "input", 1)]
    for i in range(num_inputs):
        ports += [
            BlockPort(f"s{i:02d}_axis_tdata", "input", width),
            BlockPort(f"s{i:02d}_axis_tvalid", "input", 1),
            BlockPort(f"s{i:02d}_axis_tready", "output", 1),
            BlockPort(f"s{i:02d}_axis_tlast", "input", 1),
        ]
    for j in range(num_outputs):
        ports += [
            BlockPort(f"m{j:02d}_axis_tdata", "output", width),
            BlockPort(f"m{j:02d}_axis_tvalid", "output", 1),
            BlockPort(f"m{j:02d}_axis_tready", "input", 1),
            BlockPort(f"m{j:02d}_axis_tlast", "output", 1),
        ]
    inst = BlockInstance(
        name=mod, module=mod, params={"DW": width}, ports=ports,
        description=f"AXI-Stream {num_inputs}x{num_outputs} switch",
    )
    return "\n".join(src), inst


