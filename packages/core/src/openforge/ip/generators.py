def clock_wizard(
    input_freq_mhz: float,
    outputs: list[float],
    vendor: str = "xilinx",
) -> tuple[str, BlockInstance]:
    vendor = vendor.lower()
    mod = f"clock_wizard_{vendor}_{int(input_freq_mhz)}"
    n_out = len(outputs)
    lines = [_banner(mod, f"{vendor} PLL/MMCM wrapper")]
    lines.append(f"module {mod} (")
    lines.append("    input  wire clk_in,")
    lines.append("    input  wire resetn,")
    lines.append("    output wire locked,")
    for i, _f in enumerate(outputs):
        last = i == n_out - 1
        comma = "" if last else ","
        lines.append(f"    output wire clk_out{i}{comma}")
    lines.append(");")
    if vendor == "xilinx":
        lines.append("    wire clkfb;")
        lines.append("    MMCME2_BASE #(")
        lines.append(f"        .CLKIN1_PERIOD({1000.0 / input_freq_mhz:.3f}),")
        lines.append("        .CLKFBOUT_MULT_F(8.0),")
        lines.append("        .DIVCLK_DIVIDE(1)")
        lines.append("    ) u_mmcm (")
        lines.append("        .CLKIN1(clk_in), .RST(!resetn),")
        lines.append("        .CLKFBIN(clkfb), .CLKFBOUT(clkfb),")
        for i in range(n_out):
            lines.append(f"        .CLKOUT{i}(clk_out{i}),")
        lines.append("        .LOCKED(locked)")
        lines.append("    );")
    elif vendor == "lattice":
        lines.append("    // Lattice EHXPLLL placeholder wrapper")
        lines.append("    EHXPLLL u_pll (")
        lines.append("        .CLKI(clk_in), .RST(!resetn),")
        lines.append("        .CLKOP(clk_out0),")
        lines.append("        .LOCK(locked)")
        lines.append("    );")
        for i in range(1, n_out):
            lines.append(f"    assign clk_out{i} = clk_out0;")
    else:  # gowin / default
        lines.append("    // Gowin rPLL placeholder wrapper")
        lines.append("    rPLL u_pll (")
        lines.append("        .CLKIN(clk_in), .RESET(!resetn),")
        lines.append("        .CLKOUT(clk_out0),")
        lines.append("        .LOCK(locked)")
        lines.append("    );")
        for i in range(1, n_out):
            lines.append(f"    assign clk_out{i} = clk_out0;")
    lines.append(_footer())
    ports = [
        BlockPort("clk_in", "input", 1),
        BlockPort("resetn", "input", 1),
        BlockPort("locked", "output", 1),
    ] + [BlockPort(f"clk_out{i}", "output", 1) for i in range(n_out)]
    inst = BlockInstance(
        name=mod, module=mod,
        params={"FIN_MHZ": input_freq_mhz, "N_OUT": n_out},
        ports=ports,
        description=f"{vendor} clock wizard {input_freq_mhz}MHz -> {outputs}",
    )
    return "\n".join(lines), inst


