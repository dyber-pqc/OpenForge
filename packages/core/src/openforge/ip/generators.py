def reset_synchronizer(
    num_resets: int = 1, polarity: str = "low"
) -> tuple[str, BlockInstance]:
    mod = f"reset_sync_x{num_resets}"
    released = "1'b1" if polarity == "low" else "1'b0"
    src = [_banner(mod, f"Async assert, sync release reset synchronizer (x{num_resets})")]
    src.append(f"module {mod} (")
    src.append("    input  wire clk,")
    src.append("    input  wire async_rst,")
    last = num_resets - 1
    for i in range(num_resets):
        comma = "" if i == last else ","
        src.append(f"    output wire sync_rst{i}{comma}")
    src.append(");")
    for i in range(num_resets):
        src.append(f"    reg [1:0] _ff{i};")
        src.append("    always @(posedge clk or negedge async_rst) begin")
        src.append(f"        if (!async_rst) _ff{i} <= 2'b00;")
        src.append(f"        else _ff{i} <= {{_ff{i}[0], {released}}};")
        src.append("    end")
        src.append(f"    assign sync_rst{i} = _ff{i}[1];")
    src.append(_footer())
    ports = [BlockPort("clk", "input", 1), BlockPort("async_rst", "input", 1)]
    for i in range(num_resets):
        ports.append(BlockPort(f"sync_rst{i}", "output", 1))
    inst = BlockInstance(
        name=mod, module=mod,
        params={"STAGES": 2, "POLARITY": polarity},
        ports=ports,
        description="Two-FF reset synchronizer",
    )
    return "\n".join(src), inst


