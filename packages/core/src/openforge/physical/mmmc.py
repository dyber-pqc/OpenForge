"""Multi-Mode Multi-Corner (MMMC) timing analysis configuration and runner.

This module models MMMC the way a real signoff flow does:

* A **Corner** is a PVT + RC point with its own ``.lib`` files and derating.
* A **Mode** is a functional scenario (functional, scan, low-power) with its
  own SDC and case analysis.
* A **Scenario** pairs one mode with one corner and specifies which checks
  (setup / hold / both) to run.

The :class:`MmmcConfig` can emit real OpenROAD / Innovus Tcl. The
:class:`MmmcRunner` invokes OpenSTA for each scenario and aggregates the
results into :class:`StaReport` objects using :func:`parse_sta_report` from
``physical.sta_parser``.
"""

from __future__ import annotations

import logging
import shutil
import subprocess
from pathlib import Path
from typing import Literal

from pydantic import BaseModel, Field, field_validator

from openforge.physical.sta_parser import StaReport, parse_sta_report

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Pydantic models
# ---------------------------------------------------------------------------


class Corner(BaseModel):
    """A PVT + RC signoff corner."""

    name: str
    process: str = "tt"  # ss / tt / ff
    voltage: float = 1.80  # volts
    temperature: float = 25.0  # degrees C
    lib_files: list[Path] = Field(default_factory=list)
    rc_corner: str = "typical"  # minC / nominal / maxC
    qrc_file: Path | None = None
    derate: float = 1.0  # OCV derate factor

    @field_validator("process")
    @classmethod
    def _norm_process(cls, v: str) -> str:
        return v.lower()


class Mode(BaseModel):
    """A functional mode with its own SDC + clock activation."""

    name: str
    sdc_file: Path
    active_clocks: list[str] = Field(default_factory=list)
    case_analysis: dict[str, str] = Field(default_factory=dict)


CheckType = Literal["setup", "hold", "min_max"]


class Scenario(BaseModel):
    """One (mode x corner) pair with a check type."""

    name: str
    corner: Corner
    mode: Mode
    check_type: CheckType = "min_max"


class MmmcConfig(BaseModel):
    """Complete MMMC view definition."""

    corners: list[Corner] = Field(default_factory=list)
    modes: list[Mode] = Field(default_factory=list)
    scenarios: list[Scenario] = Field(default_factory=list)

    # ------------------------------------------------------------------
    # Tcl emitters
    # ------------------------------------------------------------------

    def to_openroad_tcl(self) -> str:
        """Emit Tcl for an OpenROAD MMMC view.

        OpenROAD uses ``define_corners``, ``read_liberty -corner``, and
        ``set_operating_conditions``.
        """
        lines: list[str] = [
            "# OpenROAD MMMC view definition",
            "# Generated by openforge.physical.mmmc",
            "",
        ]

        if self.corners:
            corner_names = " ".join(c.name for c in self.corners)
            lines.append(f"define_corners {corner_names}")
            lines.append("")

        for corner in self.corners:
            lines.append(f"# ---- corner: {corner.name} ----")
            for lib in corner.lib_files:
                lines.append(f"read_liberty -corner {corner.name} {_tcl_path(lib)}")
            lines.append(
                f"set_operating_conditions -corner {corner.name} "
                f"-voltage {corner.voltage:.3f} -temperature {corner.temperature:.2f}"
            )
            if corner.derate != 1.0:
                lines.append(
                    f"set_timing_derate -corner {corner.name} -late {corner.derate:.3f}"
                )
            lines.append("")

        for mode in self.modes:
            lines.append(f"# ---- mode: {mode.name} ----")
            lines.append(f"read_sdc {_tcl_path(mode.sdc_file)}")
            for clk in mode.active_clocks:
                lines.append(f"set_propagated_clock [get_clocks {clk}]")
            for pin, value in mode.case_analysis.items():
                lines.append(f"set_case_analysis {value} {pin}")
            lines.append("")

        for sc in self.scenarios:
            lines.append(
                f"# scenario {sc.name}: mode={sc.mode.name} corner={sc.corner.name} "
                f"check={sc.check_type}"
            )
            if sc.check_type in ("setup", "min_max"):
                lines.append(
                    f"report_checks -corner {sc.corner.name} -path_delay max -group_count 20"
                )
            if sc.check_type in ("hold", "min_max"):
                lines.append(
                    f"report_checks -corner {sc.corner.name} -path_delay min -group_count 20"
                )
            lines.append("")

        return "\n".join(lines)

    def to_innovus_tcl(self) -> str:
        """Emit Tcl for Cadence Innovus ``create_constraint_mode`` /
        ``create_rc_corner`` / ``create_delay_corner`` / ``create_analysis_view``.
        """
        lines: list[str] = [
            "# Innovus MMMC view definition",
            "# Generated by openforge.physical.mmmc",
            "",
        ]

        for mode in self.modes:
            lines.append(
                f"create_constraint_mode -name {mode.name} "
                f"-sdc_files [list {_tcl_path(mode.sdc_file)}]"
            )

        lines.append("")
        for corner in self.corners:
            libs = " ".join(_tcl_path(p) for p in corner.lib_files)
            lines.append(
                f"create_library_set -name {corner.name}_libs -timing [list {libs}]"
            )
            rc_opts = (
                f"-qx_tech_file {_tcl_path(corner.qrc_file)}"
                if corner.qrc_file
                else "-T 0.025"
            )
            lines.append(
                f"create_rc_corner -name {corner.name}_rc "
                f"-temperature {corner.temperature:.2f} {rc_opts}"
            )
            lines.append(
                f"create_delay_corner -name {corner.name}_dc "
                f"-library_set {corner.name}_libs -rc_corner {corner.name}_rc "
                f"-early_derate {corner.derate:.3f} -late_derate {corner.derate:.3f}"
            )

        lines.append("")
        for sc in self.scenarios:
            lines.append(
                f"create_analysis_view -name {sc.name} "
                f"-constraint_mode {sc.mode.name} -delay_corner {sc.corner.name}_dc"
            )

        if self.scenarios:
            setup_views = " ".join(
                sc.name for sc in self.scenarios if sc.check_type in ("setup", "min_max")
            )
            hold_views = " ".join(
                sc.name for sc in self.scenarios if sc.check_type in ("hold", "min_max")
            )
            lines.append("")
            lines.append(
                f"set_analysis_view -setup [list {setup_views}] -hold [list {hold_views}]"
            )

        return "\n".join(lines)

