class MmmcRunner:
    """Execute every scenario in a :class:`MmmcConfig` through OpenSTA."""

    def __init__(self, config: MmmcConfig, design_root: Path):
        self.config = config
        self.design_root = Path(design_root)
        self.design_root.mkdir(parents=True, exist_ok=True)

    def run_all_scenarios(
        self,
        verilog: Path,
        def_path: Path | None = None,
        top: str = "top",
        opensta_binary: str = "sta",
    ) -> dict[str, StaReport]:
        """Run every scenario, return ``{scenario_name: StaReport}``."""
        results: dict[str, StaReport] = {}
        for sc in self.config.scenarios:
            logger.info("Running MMMC scenario %s", sc.name)
            script_path = self.design_root / f"{sc.name}.tcl"
            log_path = self.design_root / f"{sc.name}.log"
            script_path.write_text(
                self._scenario_script(sc, verilog=verilog, def_path=def_path, top=top)
            )
            try:
                log_text = self._run_opensta(opensta_binary, script_path, log_path)
            except FileNotFoundError:
                logger.warning("OpenSTA not found; empty report for %s", sc.name)
                log_text = ""
            report = parse_sta_report(log_text) if log_text else StaReport()
            results[sc.name] = report
        return results

    # ------------------------------------------------------------------

    def _scenario_script(
        self,
        scenario: Scenario,
        verilog: Path,
        def_path: Path | None,
        top: str,
    ) -> str:
        lines: list[str] = [f"# OpenSTA script for scenario {scenario.name}"]
        for lib in scenario.corner.lib_files:
            lines.append(f"read_liberty {_tcl_path(lib)}")
        lines.append(f"read_verilog {_tcl_path(verilog)}")
        lines.append(f"link_design {top}")
        if def_path is not None:
            lines.append(f"read_def {_tcl_path(def_path)}")
        lines.append(f"read_sdc {_tcl_path(scenario.mode.sdc_file)}")
        if scenario.corner.derate != 1.0:
            lines.append(f"set_timing_derate -late {scenario.corner.derate:.4f}")
        for pin, value in scenario.mode.case_analysis.items():
            lines.append(f"set_case_analysis {value} {pin}")
        if scenario.check_type in ("setup", "min_max"):
            lines.append("report_checks -path_delay max -group_count 20")
        if scenario.check_type in ("hold", "min_max"):
            lines.append("report_checks -path_delay min -group_count 20")
        lines.append("report_worst_slack -max")
        lines.append("report_worst_slack -min")
        lines.append("report_tns")
        lines.append("exit")
        return "\n".join(lines) + "\n"

    def _run_opensta(
        self,
        binary: str,
        script_path: Path,
        log_path: Path,
    ) -> str:
        if shutil.which(binary) is None:
            raise FileNotFoundError(binary)
        with log_path.open("w") as log_fh:
            subprocess.run(
                [binary, "-no_splash", "-exit", str(script_path)],
                stdout=log_fh,
                stderr=subprocess.STDOUT,
                cwd=str(self.design_root),
                check=False,
            )
        return log_path.read_text(errors="ignore")


# ---------------------------------------------------------------------------


def _tcl_path(p: Path | str) -> str:
    """Return a Tcl-friendly path (forward slashes, braced if spaces)."""
    s = str(p).replace("\\", "/")
    if " " in s:
        return "{" + s + "}"
    return s
