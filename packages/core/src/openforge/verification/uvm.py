    # ------------------------------------------------------------------
    # Top
    # ------------------------------------------------------------------
    def generate_top(self) -> str:
        lines: list[str] = [self.HEADER]
        lines.append("`timescale 1ns/1ps")
        lines.append("")
        lines.append('`include "uvm_macros.svh"')
        lines.append("import uvm_pkg::*;")
        lines.append(f'`include "{self.dut_name}_if.sv"')
        lines.append(f'`include "{self.dut_name}_test_smoke.sv"')
        lines.append("")
        lines.append(f"module {self.dut_name}_tb_top;")
        lines.append("    logic clk = 0;")
        lines.append("    always #5 clk = ~clk;  // 100 MHz")
        lines.append("")
        lines.append(f"    {self.dut_name}_if dut_if(.clk(clk));")
        lines.append("")
        lines.append(f"    {self.dut_name} dut (")
        port_conns: list[str] = []
        for p in self.ports:
            if p.name in ("clk", "clock"):
                port_conns.append(f"        .{p.name}(clk)")
            else:
                port_conns.append(f"        .{p.name}(dut_if.{p.name})")
        lines.append(",\n".join(port_conns))
        lines.append("    );")
        lines.append("")
        lines.append("    initial begin")
        lines.append(
            f'        uvm_config_db#(virtual {self.dut_name}_if)::set(null, "*", "vif", dut_if);'
        )
        lines.append('        $dumpfile("dump.vcd");')
        lines.append(f"        $dumpvars(0, {self.dut_name}_tb_top);")
        lines.append(f'        run_test("{self.dut_name}_test_smoke");')
        lines.append("    end")
        lines.append("")
        lines.append("    initial begin")
        lines.append("        #1ms;")
        lines.append('        `uvm_fatal("TIMEOUT", "Simulation hit watchdog timeout")')
        lines.append("    end")
        lines.append(f"endmodule : {self.dut_name}_tb_top")
        return "\n".join(lines)

    # ------------------------------------------------------------------
    # File system orchestration
    # ------------------------------------------------------------------
    def generate_all(self, output_dir: Path) -> dict[str, Path]:
        """Generate the complete UVM testbench source tree.

        Returns a dict mapping logical component name to the path of the
        generated file on disk.
        """
        output_dir = Path(output_dir)
        output_dir.mkdir(parents=True, exist_ok=True)

        files: dict[str, str] = {
            "interface": self.generate_interface(),
            "transaction": self.generate_transaction(),
            "sequence": self.generate_sequence(),
            "driver": self.generate_driver(),
            "monitor": self.generate_monitor(),
            "scoreboard": self.generate_scoreboard(),
            "agent": self.generate_agent(),
            "env": self.generate_env(),
            "test_base": self.generate_test_base(),
            "test_smoke": self.generate_test_smoke(),
            "top": self.generate_top(),
        }

        suffix_map = {
            "interface": f"{self.dut_name}_if.sv",
            "transaction": f"{self.dut_name}_transaction.sv",
            "sequence": f"{self.dut_name}_sequence.sv",
            "driver": f"{self.dut_name}_driver.sv",
            "monitor": f"{self.dut_name}_monitor.sv",
            "scoreboard": f"{self.dut_name}_scoreboard.sv",
            "agent": f"{self.dut_name}_agent.sv",
            "env": f"{self.dut_name}_env.sv",
            "test_base": f"{self.dut_name}_test_base.sv",
            "test_smoke": f"{self.dut_name}_test_smoke.sv",
            "top": f"{self.dut_name}_tb_top.sv",
        }

        out: dict[str, Path] = {}
        for key, content in files.items():
            path = output_dir / suffix_map[key]
            path.write_text(content, encoding="utf-8")
            out[key] = path

        # Also drop a small filelist to make it easy for sims.
        filelist = output_dir / f"{self.dut_name}_uvm.f"
        filelist.write_text(
            "\n".join(str(p.name) for p in out.values()) + "\n",
            encoding="utf-8",
        )
        out["filelist"] = filelist
        return out
