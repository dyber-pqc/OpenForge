def write_uvm_lite_library(target_dir: Path) -> Path:
    """Drop the uvm_lite_pkg.sv into ``target_dir``.

    Returns the absolute path to the written file.
    """
    target_dir = Path(target_dir)
    target_dir.mkdir(parents=True, exist_ok=True)
    out = target_dir / "uvm_lite_pkg.sv"
    out.write_text(UVM_LITE_BASE, encoding="utf-8")
    return out


def _test_file(test_name: str, dut_module: str, agents: list[str]) -> str:
    agent_news = (
        "\n".join(f'      m_{a} = new("{a}", this);' for a in agents) or "      // no agents"
    )
    agent_decls = "\n".join(f"  {a}_pkg::{a}_agent m_{a};" for a in agents) or "  // no agents"
    return (
        "// Auto-generated by OpenForge uvm_lite\n"
        '`include "uvm_lite_pkg.sv"\n'
        "import uvm_lite_pkg::*;\n\n"
        f"class {test_name}_env extends uvm_env;\n"
        f"{agent_decls}\n"
        f'  function new(string name = "{test_name}_env", uvm_component parent = null);\n'
        "    super.new(name, parent);\n"
        f"{agent_news}\n"
        "  endfunction\n"
        "endclass\n\n"
        f"class {test_name}_test extends uvm_test;\n"
        f"  {test_name}_env m_env;\n"
        f'  function new(string name = "{test_name}_test", uvm_component parent = null);\n'
        "    super.new(name, parent);\n"
        f'    m_env = new("env", this);\n'
        "  endfunction\n"
        "  virtual task main_phase();\n"
        f'    info("TEST", "running {test_name} against {dut_module}");\n'
        "    #1000;\n"
        "  endtask\n"
        "endclass\n"
    )


def _tb_top(test_name: str, dut_module: str) -> str:
    return (
        "// Auto-generated testbench top by OpenForge uvm_lite\n"
        "`timescale 1ns/1ps\n"
        f'`include "{test_name}_test.sv"\n'
        f"module tb_{test_name};\n"
        "  logic clk = 0;\n"
        "  logic rst_n = 0;\n"
        "  always #5 clk = ~clk;\n\n"
        f"  // DUT instantiation stub -- wire up ports manually.\n"
        f"  // {dut_module} dut(.clk(clk), .rst_n(rst_n));\n\n"
        "  initial begin\n"
        "    rst_n = 0;\n"
        "    #20 rst_n = 1;\n"
        "  end\n\n"
        "  initial begin\n"
        f"    {test_name}_test t;\n"
        f'    t = new("t");\n'
        "    t.run_phase();\n"
        "    $finish;\n"
        "  end\n"
        "endmodule\n"
    )


def _seq_file(test_name: str) -> str:
    return (
        "// Auto-generated sequences\n"
        '`include "uvm_lite_pkg.sv"\n'
        "import uvm_lite_pkg::*;\n\n"
        f"class {test_name}_base_seq extends uvm_sequence;\n"
        f'  function new(string name = "{test_name}_base_seq");\n'
        "    super.new(name);\n"
        "  endfunction\n"
        "  virtual task body();\n"
        "    uvm_sequence_item it;\n"
        "    repeat (16) begin\n"
        '      it = new("it");\n'
        "      start_item(it);\n"
        "      finish_item(it);\n"
        "    end\n"
        "  endtask\n"
        "endclass\n"
    )


def _verilator_cmd(test_name: str) -> str:
    return (
        "#!/bin/sh\n"
        f"# Build tb_{test_name} with Verilator 5.x\n"
        "verilator --binary --sv -Wno-fatal \\\n"
        "    uvm_lite_pkg.sv \\\n"
        f"    {test_name}_seqs.sv \\\n"
        f"    {test_name}_test.sv \\\n"
        f"    tb_{test_name}.sv \\\n"
        f"    --top-module tb_{test_name}\n"
        f"./obj_dir/Vtb_{test_name}\n"
    )


def generate_test_skeleton(
    test_name: str,
    dut_module: str,
    agents: list[str],
    target_dir: Path,
) -> dict[str, Path]:
    """Create a full UVM-lite test skeleton under ``target_dir``.

    Produces (at minimum):
      - uvm_lite_pkg.sv        -- base package
      - <test_name>_test.sv    -- env + test class
      - <test_name>_seqs.sv    -- base sequence
      - tb_<test_name>.sv      -- tb top
      - run_<test_name>.sh     -- verilator build/run

    Returns a dict keyed by ``pkg``, ``test``, ``seqs``, ``tb``, ``run``.
    """
    target_dir = Path(target_dir)
    target_dir.mkdir(parents=True, exist_ok=True)

    out: dict[str, Path] = {}
    out["pkg"] = write_uvm_lite_library(target_dir)

    test_path = target_dir / f"{test_name}_test.sv"
    test_path.write_text(_test_file(test_name, dut_module, agents), encoding="utf-8")
    out["test"] = test_path

    seq_path = target_dir / f"{test_name}_seqs.sv"
    seq_path.write_text(_seq_file(test_name), encoding="utf-8")
    out["seqs"] = seq_path

    tb_path = target_dir / f"tb_{test_name}.sv"
    tb_path.write_text(_tb_top(test_name, dut_module), encoding="utf-8")
    out["tb"] = tb_path

    run_path = target_dir / f"run_{test_name}.sh"
    run_path.write_text(_verilator_cmd(test_name), encoding="utf-8")
    out["run"] = run_path

    return out
