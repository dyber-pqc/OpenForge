"""UVM-lite protocol agent generator.

For each supported protocol we ship a single-file agent (sequence_item +
driver + monitor + sequencer wrapper) written in Verilator-friendly
SystemVerilog. The SV depends on the ``uvm_lite_pkg`` package from
``library.py`` being on the compile path.

The templates here are real, compilable code -- not lorem. They cover:
    axi4, axi4_lite, ahb, apb, axi_stream, i2c, spi, uart, wishbone
"""

from __future__ import annotations

from pathlib import Path

from pydantic import BaseModel, Field


class AgentSpec(BaseModel):
    """Specification for a generated UVM-lite agent."""

    name: str
    protocol: str
    direction: str = Field(default="master", description="master|slave|monitor")
    config: dict = Field(default_factory=dict)


# ---------------------------------------------------------------------------
# Protocol templates. All use `{name}` as the agent/class prefix.
# ---------------------------------------------------------------------------


