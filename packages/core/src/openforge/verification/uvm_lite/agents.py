def list_protocols() -> list[str]:
    """Return the protocol identifiers we can emit agents for."""
    return sorted(_TEMPLATES.keys())


def get_protocol_template(protocol: str) -> str:
    """Return the raw template string for ``protocol``.

    Raises:
        KeyError: if the protocol is not known.
    """
    if protocol not in _TEMPLATES:
        raise KeyError(f"unknown protocol: {protocol!r}")
    return _TEMPLATES[protocol]


def generate_agent(spec: AgentSpec, output_dir: Path) -> Path:
    """Render an agent file for ``spec`` into ``output_dir``.

    The generated file is ``<name>_agent.sv`` and depends on
    ``uvm_lite_pkg.sv`` being compiled alongside it.
    """
    template = get_protocol_template(spec.protocol)
    # str.format would choke on the many SV braces, so do a plain replace.
    body = template.replace("{name}", spec.name)
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    out = output_dir / f"{spec.name}_agent.sv"
    out.write_text(body, encoding="utf-8")
    return out
