"""openforge project management commands -- init, open, info, validate."""

from __future__ import annotations

import json as json_mod
import sys
from enum import Enum
from pathlib import Path
from typing import Optional

import typer
from rich.console import Console
from rich.panel import Panel
from rich.table import Table

console = Console()

app = typer.Typer(
    name="project",
    help="Project management commands.",
    no_args_is_help=True,
)


# ---------------------------------------------------------------------------
# Enums
# ---------------------------------------------------------------------------


class ProjectKind(str, Enum):
    asic = "asic"
    fpga = "fpga"
    pcb = "pcb"
    mixed = "mixed"


class TemplateName(str, Enum):
    sky130 = "sky130"
    ice40 = "ice40"
    ecp5 = "ecp5"
    gowin = "gowin"
    caravel = "caravel"
    pcb = "pcb"
    empty = "empty"


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _load_config(project_dir: Path):
    """Load config or exit with error message."""
    from openforge.config.loader import ConfigNotFoundError, load_config

    try:
        return load_config(search_dir=project_dir)
    except (FileNotFoundError, ConfigNotFoundError):
        console.print(
            f"[red]Error:[/] no openforge.yaml found in [cyan]{project_dir}[/]. "
            "Run [bold]openforge init[/] first."
        )
        raise typer.Exit(code=1)


def _write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    console.print(f"  [dim]create[/] {path}")


