_YAML_ASIC = """\
# OpenForge EDA project configuration
project:
  name: "{name}"
  top_module: "{name}"
  target_pdk: "{pdk}"

design:
  sources:
    - rtl/*.sv
    - rtl/*.v
  includes: []
  constraints:
    - constraints/timing.sdc

verification:
  simulation:
    tool: verilator
    testbenches:
      - tb/tb_{name}.sv
    coverage:
      line: true
      toggle: true

  formal:
    tool: symbiyosys
    properties: []
    depth: 100
"""

_YAML_FPGA = """\
# OpenForge EDA project -- FPGA
project:
  name: "{name}"
  top_module: "{name}"

design:
  sources:
    - rtl/*.sv
    - rtl/*.v
  includes: []
  constraints:
    - constraints/pins.{constraint_ext}

verification:
  simulation:
    tool: icarus
    testbenches:
      - tb/tb_{name}.sv
    coverage:
      line: true
"""

_YAML_PCB = """\
# OpenForge EDA project -- PCB
project:
  name: "{name}"
  top_module: "{name}"

design:
  sources: []
  includes: []
  constraints: []
"""

_RTL_COUNTER = """\
// Simple up-counter generated by OpenForge
module {name} #(
    parameter WIDTH = 8
) (
    input  logic clk,
    input  logic rst_n,
    input  logic en,
    output logic [WIDTH-1:0] count
);
    always_ff @(posedge clk or negedge rst_n) begin
        if (!rst_n)
            count <= '0;
        else if (en)
            count <= count + 1;
    end
endmodule
"""

_TB_TEMPLATE = """\
`timescale 1ns / 1ps
module tb_{name};
    logic clk = 0;
    always #5 clk = ~clk;
    logic rst_n;

    {name} dut (
        .clk   (clk),
        .rst_n (rst_n)
    );

    initial begin
        rst_n = 0;
        #20 rst_n = 1;
        #200;
        $finish;
    end
endmodule
"""

_SDC_TEMPLATE = """\
create_clock -name clk -period {period} [get_ports clk]
"""


